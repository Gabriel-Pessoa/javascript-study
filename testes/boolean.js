// Operador || (OU) retorna a primeira assertivas verdadeira
console.log(1,true || false); //1.true
console.log(2,false || true);//2.true
console.log(3,true || true);//1.true
console.log(4,false || false);//2.false

// Operador E (&&) retorna true se as duas assertivas forem verdadeiras, retorna false se 1 ou as 2 assertivas forem falsas
console.log(1,true && false)
console.log(2,false && true)
console.log(3,true && true)
console.log(4,false && false)